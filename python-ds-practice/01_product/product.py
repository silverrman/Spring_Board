def product(a, b):
    """Return product of a and b.
        >>> product(2, 2)
        4

        >>> product(2, -2)
        -4
    """
    # This function takes two parameters (a and b) and returns their product
    # The multiplication operator (*) works with integers, floats, and even for
    # other use cases like string/list repetition
    # Time complexity: O(1) - constant time operation
    # Space complexity: O(1) - no additional space needed based on input size
    return a * b
